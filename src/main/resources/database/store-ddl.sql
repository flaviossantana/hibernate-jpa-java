create table produtos
(
    id             bigint generated by default as identity,
    categoria      varchar(255),
    data_cadastro  date,
    desc_descricao varchar(255),
    desc_nome      varchar(255),
    vlr_preco      numeric(19, 2),
    primary key (id)
);

create table categorias
(
    id        bigint generated by default as identity,
    desc_nome varchar(255),
    primary key (id)
);

create table clientes
(
    id        bigint generated by default as identity,
    numr_cpf  varchar(255),
    desc_nome varchar(255),
    primary key (id)
);

create table itens_pedido
(
    id           bigint generated by default as identity,
    qtd_produto  integer,
    vlr_unitario numeric(19, 2),
    id_pedido    bigint,
    id_produto   bigint,
    primary key (id)
);

create table pedidos
(
    id          bigint generated by default as identity,
    data_pedido timestamp,
    vlr_total   numeric(19, 2),
    cliente_id  bigint,
    primary key (id)
);
